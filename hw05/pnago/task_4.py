# Вводиться нормалізований текст, який крім слів може містити певні знаки пунктуації.
# Програма будує список слів, знаки пунктуації виключаються.
# Під нормалізованим текстом будемо розуміти текст,
# в якому пробіл ставиться після знаків пунктуації, за винятком відкриває дужки (пробіл перед нею).

str = input("Write down or insert some text:\n")

punctuation = ['.',',',':',';','!','?','(',')']

for p in punctuation:
    str = str.replace(p, ' ')

wordList = str.split()

print(wordList)